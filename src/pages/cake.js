import Layout from "../components/layout"
import React from "react"

const CakePage = () => (
  <Layout>
    <h3>You will need…</h3>
    <ul>
      <li>180g butter, cubed</li>
      <li>1 1/3 cups sugar</li>
      <li>2 tsp vanilla extract</li>
      <li>3 eggs</li>
      <li>2 cups ground almonds*</li>
      <li>1 cup white spelt flour</li>
      <li>3 tsp baking powder</li>
      <li>1/2 cup coarsely chopped walnuts</li>
      <li>1/2 cup cacao nibs</li>
      <li>A small handful of sunflower and pumpkin seeds</li>
      <li>1/2 – 3/4 cup milk (enough to give you a not-too-dry but not-runny-either consistency)</li>
      <li>300g frozen raspberries</li>
    </ul>

    <h3>How to make…</h3>
    <ul>
      <li>Pre-heat oven to 180°C</li>
      <li>Beat the butter, sugar and vanilla until white and fluffy.</li>
      <li>Then, slowly – one at a time, beat in the eggs, beating well after each egg. If you add them all at once or
        too quickly the mixture will curdle.
      </li>
      <li>In a separate bowl mix together the flours and the baking powder with a wooden fork.</li>
      <li>Add in the walnuts, cacao nibs and seeds and mix until combined.</li>
      <li>Add the flour mix to the butter mix, and add the milk.</li>
      <li>Gently fold together with a spatula.</li>
      <li>Pour into a lined* 12″ tart tin (I use the amazing cake tin liners from Lakeland) and cover with a single
        layer of frozen raspberries.
      </li>
      <li>Bake for 50 minutes or until golden brown and firm. If the cake is still wobbly in the middle it’s not done!
      </li>
      <li>Test for readiness by inserting a skewer or knife into the cake. If it comes out with only a few grains of
        ground almonds it’s ready. This cake is better a bit undercooked than overcooked.
      </li>
      <li>This cake is rather good warm.</li>
    </ul>
  </Layout>
)

export default CakePage